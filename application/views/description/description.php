                                        <div class="item active" style="font-size: 20px;">
                                            <b style="font-size: 23px;">創立緣起</b><br><br><br>
                                            <img class="img-thumbnail" src="img/MainFig_1.png" width="60%"><br><br>
                                            <p>國立高雄第一科技大學秉持建校宗旨與使命責任，由曾擔任工研院副院長之谷家恆前校長帶領下，參考國內外學術與產業發展所遭遇之困境及本校特色，於民國86年4月因應經濟部中小企業處於民國85年訂定「鼓勵公、民營機構設立中小企業創新育成中心要點」，成立國立高雄第一科技大學創新育成中心並推動。</p><br>
                                            <p>「實務與理論兼具、教學與研究並重、專業與通識結合、人文與科技相融、創新與科技兼容」等五大設校理念，共同肩負地區各產業之中小企業輔導工作為職志，並整合學校資源，建構良好的培育環境。此外，中心致力於校內資源整合力圖建構良好的廠商培育環境，並以肩負南區各產業之新創及創新中小企業轉導工作為職志出人出力，以協助企發初期之平穩發展。</p><br>
                                            <p>本中心歷任主管產、官、學經歷豐富，校內外資源(含空間、設備、技術及研發人力等)豐沛，對中小企業開發新產品及新技術，促進企業創業與創新等均可提供協助並降低進駐廠商創業及研發初期成本與風險。</p>
                                        </div>
                                        <div class="item" style="font-size: 20px;">
                                            <b style="font-size: 23px;">中心任務</b><br><br><br>
                                            <p>
                                                1.協助產業孕育產品創新計畫、開發新技術與新產品。<br><br>
                                                2.減輕中小企業創業過程的研究投資費用與風險，增進初創業者或開發新產品與新製程之成功率。<br><br>
                                                3.提供產學合作場所，協助產業研發，促進各項技術轉移，甚至技術交易。<br><br>
                                                4.提供產品測試服務、資訊提供及營運管理之諮詢服務，加速產品開發，促使研究成果商品化。<br><br>
                                                5.結合學校資源辦理訓練班協助企業人才培訓與教育訓練。<br><br>
                                                6.針對地區特色產業與聯盟共同合辦產業技術與管理服務專業研討會，提供區域產業各項新知之學習機會。<br><br>
                                            </p>
                                        </div>
                                        <div class="item" style="font-size: 20px;">
                                            <b style="font-size: 23px;">組織架構</b><br><br><br>
                                            <img class="img-thumbnail" src="img/org_redraw_3.png"><br><br>
                                            <p>
                                                中心主任：主任由校長聘請副教授(含)以上專任教師兼任，綜理中心之經營管理、營運方針與策略制定。<br><br>
                                                審查委員：具有產學合作經驗之產、官、學等成員。由中心主任召集並負責中心各式工作。<br><br>
                                                輔導專家：本校各系所專任教師及校內外專家等成員。<br><br>
                                                專案經理：具育成輔導專長並自主協助中心專案工作推動者。<br><br>
                                            </p>
                                        </div>

                                        <div class="item" style="font-size: 20px;">
                                            <b style="font-size: 23px;">培育領域</b><br><br>
                                            <p>綜合產業的特性，從高科技產業的深化與廣化、傳統產業的升級轉型以及推動新興新創產業等三個層面加以發展，結合本校各院系所以及研究中心等相關資源，進行企業創新與創業之培育工作。</p><p><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">主要提供</b><br>
                                            </p><p>
                                            1.進駐空間與設備<br>
                                            2.政府補助計畫撰寫<br>
                                            3.技術與營運諮詢服務<br>
                                            4.協助產品或成果行銷推廣<br>
                                            5.規劃專業訓練/檢測分析<br><br>
                                            </p>

                                            <b style="font-size: 20px; color:#fcbb3a;">主要培育領域</b><br>
                                            <p>1.精密機械產業    2.綠能環保產業    3.資通訊產業</p><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">主要培育技術</b><br>
                                            <p>
                                            1.機械設計分析與精密製造、機電控制與系統整合<br>
                                            2.光機電系統整合與控制、光電訊息處理與應用技術<br>
                                            3.電子商務與網路安全、電腦與通訊整合<br>
                                            4.綠色能源、物流倉儲管理與行銷規劃等技術<br>
                                            5.生技及生化檢測與資料分析技術<br>
                                            </p>
                                        </div>

                                        <div class="item" style="font-size: 20px;">
                                            <b style="font-size: 20px;">培育資源</b><br><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">空間與設備</b><br>
                                            <p>
                                            （一）提供進駐空間及基本辦公設備。<br>
                                            （二）提供共用實驗設備、機械儀器與公共設施。</p><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">商務支援</b><br>
                                            <p>
                                            （一）成立專家諮詢小組，提供營運諮詢服務。<br>
                                            （二）協助宣傳展覽、推廣與各項研討會或說明會。<br>
                                            （三）規劃專業訓練及實務專題。<br>
                                            （四）提供投融資資訊或引介創業投資公司。</p><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">行政支援</b><br>
                                            <p>
                                            （一）共通性行政事務支援。<br>
                                            （二）協助向各界申請相關輔導計畫。<br>
                                            （三）指導撰寫營運計畫書。<br>
                                            （四）協助建構各項對內或對外合約。</p><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">技術及人才支援</b><br>
                                            <p>
                                            （一）結合本校師資或業界，提供技術諮詢或委託研究等服務。<br>
                                            （二）人力資源招募（工讀生、研究助理等）。<br>
                                            （三）協助以產學合作或技術移轉方式引進新技術。<br>
                                            （四）促進科技研發單位合作及結盟。</p><br><br>
                                            <b style="font-size: 20px; color:#fcbb3a;">資訊支援 </b><br>
                                            <p>
                                            （一）引介各項專業顧問。<br>
                                            （二）提供專業團體如同業公會、專業學會、協會及地方性工業策進會等組織之合作網脈。<br>
                                            （三）促成廠商經營發展之策略聯盟，以及育成廠商間市場、行銷 、通路、融資、集資等合作機會。<br>
                                            （四）掌握園區或工業區等相關資訊與申請模式，協助提供廠商畢業後發展空間之資訊 。</p><br><br>
