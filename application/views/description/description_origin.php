	        <p>國立高雄第一科技大學秉持建校宗旨與使命責任，由曾擔任工研院副院長之谷家恆前校長帶領下，參考國內外學術與產業發展所遭遇之困境及本校特色，於民國86年4月因應經濟部中小企業處於民國85年訂定「鼓勵公、民營機構設立中小企業創新育成中心要點」，成立國立高雄第一科技大學創新育成中心並推動。</p><br>
