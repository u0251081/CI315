            <p>本中心歷任主管產、官、學經歷豐富，校內外資源(含空間、設備、技術及研發人力等)豐沛，對中小企業開發新產品及新技術，促進企業創業與創新等均可提供協助並降低進駐廠商創業及研發初期成本與風險。</p>
	    
